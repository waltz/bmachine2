			<h4>Video Playback and Fullscreen</h4>
			<p>Videos play on Democracy Player in the large main window of the screen, with playback controls below the video that you are watching.  Democracy Player also plays video fullscreen.  It has beautiful fullscreen controls on Mac and pretty good fullscreen controls on Windows and Linux.</p>

			<img src="<?= $base ?>/images/fullscreen_screen.jpg" />
			<div class="screendesc">Democracy fullscreen controls</div>
			<Br />
			<p>iTunes video playback strikes me as a little bit odd.  When you play a video, it first appears in the tiny box in the lower left hand side of the interface, where album cover art usually appears.  If you click on this box, the video pops up into a separate window.</p>
			
<p>I believe Democracy Player offers a video playback experience that is superior to iTunes in simplicity and ease of use.</p>
			
			<h4>iPod Syncing</h4>
			<p>Democracy Player does not currently sync to iPod, but we're working on adding that functionality in an upcoming version. Until then, for getting videos to your video iPod, iTunes has Democracy Player beat.</p>
			
			<h4>Content Access and Content Quality</h4>
			<p>iTunes' limited support for video formats restricts which video RSS feeds you can subscribe to in iTunes. Limitations like this can discourage diversity and independence in video publishing. There are thousands of video RSS feeds in other formats, such as XVid or Windows Media, that iTunes for Windows can't play. Democracy Player does support these feeds.</p>
			
			<p>In addition, Democracy Player supports BitTorrent feeds and also works as a standalone BitTorrent client.  As a user, this means that you have access to videos and video feeds that iTunes can't download.  Most importantly, BitTorrent technology lets video makers publish very large, very high quality video files without paying for all that bandwidth.  There are already dozens of high-resolution video channels in the Democracy Channel Guide, but these channels won't work in iTunes.</p>
			
			<p>Lastly, Democracy Player's built-in Channel Guide lists more than 500 video channels, compared to only about 150 browsable channels in iTunes' video podcast directory.  Democracy Player can play any feed that works with iTunes and most feeds that are in the iTunes directory are also in the Democracy Channel Guide.</p>
			
			<h4>One-Click Subscribe</h4>
			<p>iTunes and Democracy Player both let users click on a button on a website to subscribe to a feed.  However, <a href="http://subscribe.getdemocracy.com/">Democracy Player's 1-Click System</a> goes beyond iTunes' in two ways.  First, publishers can create 1-click subscribe buttons for batches of channels, not just a single channel.  That means that if you publish more than 1 channel, you can subscribe users to everything you've got with a single click.  Even better, Windows users that don't have Democracy Player installed already can download a version of the player that comes with your channels instead of the standard default channels.  It's like your own branded version of Democracy Player.</p>
			
			<h4>Openness</h4>
			<p>Democracy Player is free, open-source software, built by a non-profit organization.  Being open means that anyone can view the source-code for the program and change it if they want to make something that works differently.   In addition, Democracy Player does an excellent job of implementing RSS standards.  iTunes is closed-source software.</p>
			
			<h4>Linux</h4>
			<p>Democracy Player runs on Linux.  There is no iTunes for Linux.</p>


