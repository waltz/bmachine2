<div class="help_pop">
<h2>Help: Publishing a video</h2>

<h3>Posting a File</h3>

<p>
You can post a file either by sharing it from your computer ("Post a Torrent"), by uploading it directly to your server ("Upload a File"), or simply by linking to the URL of a file that is already available on the web ("Specify URL").
</p>

<h3>Posting with BitTorrent</h3>


<p>
To post a file from your computer using BitTorrent, click "Post a Torrent" on the publish screen.  If you've never done this before, you'll need to download Broadcast Machine Helper first.  Links to the download are located below the 'Post a Torrent' button.</p>

<p>
Once you have Broadcast Machine Helper installed, when you click 'Post a Torrent' you will be asked to choose a file to upload.  Once you choose a file, you can return to the publish screen and continue adding information about the file.  <strong>For your files to be available, you must keep the helper application open and sharing those files.</strong>
</p>

<p>
If you're interested in sharing the file from your server using BitTorrent, click on the 'Settings' tab in Broadcast Machine to learn about server-sharing.
For more about posting files and using BM Helper, see the <a target="_blank" href="http://www.participatoryculture.org/broadcast/help">BM FAQ</a>.
</p>

<h3>Entering Basic Info</h3>

<p>
Once you've picked a video to publish, you should enter some basic information that will tell viewers about the video.  Most of these are self, explanatory, but here are a few notes:
</p>

<p>
<strong>Publish to These Channels:</strong> to have the video appear on a Channel, you must select it. You can publish a video to more than one channel at a time.
</p>

<p><strong>Thumbnail:</strong> an image from the video that is representative or enticing to potential viewers. Images should generally be JPG, GIF, or PNG format. Bigger images are better (up to 200k) and Broadcast Machine will resize them as needed.</p>

<p><strong>Creative Commons license:</strong> If you are the copyright holder for the file, you can choose to publish it with a Creative Commons license. These licenses will allow other people to use your work in ways that you specify, so that they can share and build on your creativity.</p>

<h3>Optional: Additional Info</h3>

<p>The more information you include about your video, the more ways people will be able to find it by searching, sorting, and filtering. For example, adding information about where the video was made will eventually let people discover interesting media being made in their own backyard.</p>

