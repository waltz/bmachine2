<h2>Torrent Troubleshooting</h2>
</div>

<p>Posting a torrent file can be tricky at first, but it will reduce your bandwidth costs to virtually zero.  This is very important for most sites that host high quality videos.  Broadcast Machine combines a number of technologies that make torrent posting easier than ever before-- and very managable for most sites.</p>

<p><strong>The most common cause of problems with torrent files is that the BM Helper window must remain open for the torrent file to be available on your site.</strong>  If you use server-sharing, you can avoid this problem, but it will require using some bandwidth and disk space.  See below.</p>

<h3>I clicked "Post a Torrent" but nothing happened.</h3>

<p>
To post a torrent, you'll need to first install Broadcast Machine helper.  There's a link to download the helper for mac or windows on the "Publish" tab, in the file posting box.  Once you download it, you'll need to install it.  
</p>

<p><strong>On Windows:</strong> run the BM Helper file that you download.  It will ask you to quit your browsers so that it can install the plugin which simplifies uploading torrents.
</p>

<p><strong>On Mac:</strong> you need to drag 'BM Helper' into your applications folder and run the program once, after quiting your web browser.  This will install the plugin that helps simplify torrent posting. 
</p>

<p>After you've installed BM Helper, when you click "Post a Torrent" you should be prompted to choose a file that you'd like to publish.</p>


<h3>I shared a file, but it won't download.</h3>

<p>Testing whether a torrent is working can be harder than getting it to work.  Paradoxically, just because you aren't able to download the file you posted, doesn't mean that it's not working properly (ok, that's not actually a paradox, but it is odd).  Here's some reasons why you might not be able to download a file that you posted:</p>

<p>1. <em>You closed the BM Helper window that was sharing the file.</em>  If you closed the window in BM Helper that opened when you posted the file, or if you quit BM Helper, restarted your computer, turned off your computer, etc-- the file will no longer be shared from your computer.  To start sharing the file again, you'll need to go into the 'files' tab and click 'download' on the file that you want to share.  BM Helper may ask you where you want to save the file-- instead of picking a place to save the file, go into the folder where the file is located and select that folder.  BM Helper may act like it is going to overwrite the file, but in fact it will recognize that you have the complete file and resume sharing it.</p>

<p>2. <em>You can't download a torrent from the same computer that you're sharing it.</em>  This just won't work correctly in most situations.</p>

<p>3. <em>You may not be able to download a torrent from a computer that's on the network / router / broadband connection as the computer that is sharing the file.</em>  Ask a friend somewhere else to give it a try.</p>

<p>4. <em>If the computer sharing the file and the computer attempting to download the file are both on internet connections that have firewalls (virtually every common broadband router has a firewall) then you might not be able to transfer the file.</em>  This makes testing a torrent difficult, but in practice doesn't prevent people from getting the file.  This is because even if a single computer that's attempting to download the file is on a connection with no firewall or has forwarded the ports on their router, they will be able to help the computer that are behind a firewall work around it. </p>

<p>5. <em>It might just be slow to get started.</em>  Sometimes torrent downloads can take 5 minutes or more to get started.  This might make it appear that the download is not working.  Be patient.</p>

<p>Once you succesfully share a few files, you won't need to test every one to be confident that they are working.</p>

