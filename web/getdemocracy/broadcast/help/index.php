<h3>How do I get started?</h3>

<p>
Our <a href="http://www.getdemocracy.com/make/channel-guide/">getting started guide</a> is a good overview of how you can use Broadcast Machine to make a video channel.  After you check it out, you can come back here for answers to more specific questions.
