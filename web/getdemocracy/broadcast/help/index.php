<h3>I'm having trouble posting a torrent file.</h3>

<p>Broadcast Machine does a lot to simplify the process of sharing files using BitTorrent, but making torrents can still be finicky.  Here's a page for help: <a href="torrent_posting.php">Torrent Troubleshooting</a>.


<h3>What's a Channel?</h3>

<p>A 'Channel' is a link that anyone using <a href="http://www.getdemocracy.com/">Democracy Player</a> or similar internet video software can subscribe to, simply by pasting the link into their software program.  The terms "video blog", "video podcast", and "video RSS feed" all have specific meanings, but are all types of internet TV channels and use the same underlying technologies.  Broadcast Machine creates these kinds of channels.  Once they've subscribed, users will be see a notification whenever new files (videos, for example) are posted to that channel.  The channel creates a continuous connection to the user.
</p>

<p>With Broadcast Machine you can easily create Channels: go to the Channel tab, click 'create a new channel', and enter a name/description of your choice.  When you post a file or link to a file with Broadcast Machine, those files become part of any channels that you choose to publish them to.</p>

<p>In technical terms, the Channels that Broadcast Machine makes are RSS 2.0 feeds with a link to the enclosed file and information about it that the publisher has entered (Yahoo Media RSS style).</p>

<h3>How do people subscribe to my Channel?</h3>

<p>
Anyone can watch your video Channel using <a href="http://www.getdemocracy.com/">Democracy Player</a>, by clicking 'Add Channel' and pasting in the URL to the RSS feed for that particular channel.  We also suggesting sumbitting your Channel to the DTV Channel Guide, which is built in to the application.  <a href="https://channelguide.participatoryculture.org/?q=submitchannel">Submit your channel here >></a>
</p>

<h3>Can I make a video channel by linking to videos I find on the internet?</h3>

<p>
Yes.  In fact, this is the easiest way to make a channel, because you don't need to worry about uploading or torrenting the file.  On the publish page, click 'link to a file' and enter a URL (link) that points to a video file or a .torrent for a video file.  Enter info about the video and click publish.  That video will be published to your channel.
</p>


<h3>Can I use Broadcast Machine for audio podcasting?</h3>

<p>Yes.  Broadcast Machine will publish any kind of file (audio, video, text, etc) and create channels for your content.  You might see us mention video more often, because creating video channels was a big motivation for building Broadcast Machine, but the software is completely flexible as to content type.
</p>

<h3>Is Broadcast Machine compatible with iTunes?</h3>

<p>
iTunes does not completely follow the correct standards for dealing with RSS feeds and was not able to read early Broadcast Machine feeds.  However, in the newest version of Broadcast Machine we added a setting for 'Direct URLs' that makes possible for iTunes to read the feed (turning on direct URLs also works better with some search engines).  The Direct URLs setting will work on most servers, but not all.  To test it, check off the box on the settings page, save changes, and then view the front page of your Broadcast Machine install.  Try downloading one of your files-- if the file works as usual, Direct URLs is working fine.  If not, contact your server administrator for assistance.
</p>


