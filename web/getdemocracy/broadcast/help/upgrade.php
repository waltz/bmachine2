<h2>How to Upgrade Broadcast Machine</h2>
</div>
<p>Upgrading your version of Broadcast Machine is easy and you won't need to repost your files.</p>  <p>Here's how it goes:</p>

<ul>
<li>1. To be safe, use your FTP client to download your entire Broadcast Machine directory as a backup in case something goes wrong.</li>
<li>2. Special instruction for users upgrading to version 20 - Turn off MySQL support during the upgrade.
<li>3. Get the current version of the software: <a href="http://www.participatoryculture.org/bm/bm.zip">download Broadcast Machine</a>.</li>
<li>4. Unzip that file and upload the entire contents of the "bm" directory into the directory where you run Broadcast Machine on your server.  Your 
ftp client will ask you if you want to overwrite old files, say yes to all.</li>
<li>5. Most of the time, that's it!  Just point your browser to your Broadcast Machine installation.  If there's anything left for you to do (like 
create a directory or change permissions) you'll see intstructions.</li>
</ul>


