<h2>Planning Your Video Channels</h2>

<h3>RSS Compatibility</h3>
<p>DTV Channels are RSS feeds.  Broadcast Machine generates feeds with metadata information that users enter about the video.  But you don't need Broadcast Machine to make compatible feeds.  DTV can read any RSS feed.</p><p> This means that if you have a website that generates RSS feeds of your video content, you might already be compatible with DTV.  However, the format that Broadcast Machine publishes can include thumbnail images, description, and other information that make the viewer experience of your channel a lot better.  You should consider making your feeds conform to the <a href="http://www.participatoryculture.org/wiki/index.php?title=RSS_Extensions">RSS format</a> that we are suggesting.  You can do this either by using Broadcast Machine as a publishing tool or by tweaking your existing publishing or content management tool to generate compatible feeds.</p>

<h3>Channel Flexibility and Community Involvement</h3>
<p>Since RSS is so simple and flexible, it's possible for publishers to create a variety of Channels on a single website.  These Channels can have overlap and intersect in any way that is useful for your viewers.</p>

<p>For example: if you have a music video site, you might want to offer a hip-hop channel and a rock channel and a pop channel.  You could also have an 'everything' channel that would publish videos from all of those genres together on a single channel.</p> 
<p>Another example: if you have a site about surfing, you could have a channel of the best videos and a channel of user submitted videos.  If a user submits a video that you really like, you can add it to the more carefully edited 'best videos' channels.  This way you can invite your community to participate openly while presenting only the best stuff to viewers who want a more edited presentation.</p>

<p>Broadcast Machine easily facilitates these kinds of participation.  If you want to go to an even higher level of community participation, you'll likely need to do some custom programming.  An example of this: let's say you have a site where each of your users has their own homepage and blog.  Let's say you also want to give them the ability to post videos.  Each of those users could post videos to the site and could have their own channel link that would let people subscribe to the content that they make.</p>

