<h2>User Experience of Video Channels</h2>

<p>Our video player works with channels in two ways:</p>

<p>The first is a typical RSS feed, similar to the ones that are available on most blogs.  RSS is an open standard for regularly updated content.  In this case the RSS feeds will contain links to video files-- when a new video is published, the RSS feed will contain a new entry.  Our video player will regularly check all RSS 'channels' that a user has subscribed to.  When the RSS feed has new content, the player will add the item to the download queue and notify the user when a new video is ready to be watched.  This way of using a channel is perfect for any video content that intended to be presented to viewers on an ongoing basic: news, shows, music videos, video blogs, etc.  
</p>

<p>The other way to approach a channel is more like a library: the player will present archives of the content in a channel that users can browse and select from.  The iTunes Music Store is a good analogy-- users browse the store and choose what they would like to download.  Our publishing software will automatically make a customizable library display for every channel.</p>

