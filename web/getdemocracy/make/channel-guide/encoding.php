<h2>Compressing and Encoding Videos</h2>

<p>This, of course, is a necessary step for publishing videos.  Our publishing software works with any kind of file and our video player will be able to play most formats, so for people who already have digitzed content, you're already ready to go. If you haven't put your work into a digital format, or are looking for the best format for this platform, there's a whole world of advice about how to digitize content, but we should make a couple points here as well:</p>

<p><strong>You can go big.</strong>  Our video player downloads files in the background and our p2p publishing software takes the bandwidth load off your back.  This means you can offer big, full screen video-- and that makes everyone happy.  At a certain point really giant video files will suck up a user's drive space and will take longer to download, but the days of tiny, jerky, web video clips are over.</p>

<p><strong>Make it easy.</strong>  The most important thing is to settle into a simple process for preparing videos-- messing with video formats can be a headache.  However, that best process will depend on what computers and software the video makers use.  Here are some helpful rules of thumb:</p>

<ul>
<li>Don't use Real or Windows Media format, since our player won't support it completely.</li>
<li>Otherwise, our player will play most major formats.</li>
<li>If you can, we recommend making files using the MPEG 4 codec that end in the extension .mp4.  Our player will play these, and so will Quicktime.</li>
<li>If you want full DVD quality, use MPEG2, which is the DVD format.  However, MPEG2 files are much larger than MPEG4 and will take longer to download.</li>
<li>If your video is already on a DVD, try <a href="http://handbrake.m0k.org/">Handbrake</a> (Mac) or <a href="http://gordianknot.sourceforge.net/modules.php?op=modload&name=Downloads&file=index&req=viewdownload&cid=1">Gordian Knot</a> (Windows) to make publishable files from the DVD.</li>
<li>If your video is in iMovie, just go to File > Share, select the "Quicktime" tab, and select "Compress Movie for: CD-ROM"</li> 
</ul>

